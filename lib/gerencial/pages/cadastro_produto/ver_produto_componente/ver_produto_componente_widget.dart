class _VerProdutoComponenteWidgetState
    extends State<VerProdutoComponenteWidget> {
  late VerProdutoComponenteModel _model;

  @override
  void setState(VoidCallback callback) {
    super.setState(callback);
    _model.onUpdate();
  }

  @override
  void initState() {
    super.initState();
    _model = createModel(context, () => VerProdutoComponenteModel());

    WidgetsBinding.instance.addPostFrameCallback((_) => setState(() {}));
